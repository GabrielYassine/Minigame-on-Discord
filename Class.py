class Player:
    def __init__(self, gamemode = 0, strength =1, health =1, armor_penetration =1, movement_speed = 0, kills =0, money =0, time =0):
        self.gamemode = gamemode
        self.strength = strength
        self.health = health
        self.armor_penetration = armor_penetration
        self.movement_speed = movement_speed
        self.kills = kills
        self.money = money
        self.time = time
