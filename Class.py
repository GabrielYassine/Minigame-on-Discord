class Player:
    def __init__(self, gamemode = 0, strength =1, health =1, armor_penetration =1, kills =0, money =0, time =0):
        self.gamemode = gamemode
        self.strength = strength
        self.health = health
        self.armor_penetration = armor_penetration
        self.kills = kills
        self.money = money
        self.time = time
